import nltk
from nltk.stem.porter import PorterStemmer
ps=PorterStemmer()

def stem(text):
  y=[]
  for i in text.split():
     y.append(ps.stem(i))
  return " ".join(y)

#apply function will work as for loop, for each row the function will be called.
