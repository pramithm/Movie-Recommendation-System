"""5. Vectorization

Machines only understand numbers. Vectorization is the translator.
Every type of raw data — whether it's text, images, video, audio, or anything else — gets converted into numbers (vectors) so that machine learning models can understand and process it.

Example:<br>
Text    "Hello World"        →   [0.2, 0.8, -0.3, 0.5, ...]<br>
Image   (a photo of a cat)  →   [255, 230, 128, 0, 45, ...]<br>
Video   (a movie clip)      →   [frames → each frame → pixels → numbers]<br>
Audio   (a song / voice)    →   [frequency values → [0.4, 0.1, 0.9, ...]]<br>
Number  Age = 25            →   [25] or scaled → [0.25]<br>
Category "Red"              →   [1, 0, 0]
"""

#Listen Vectorization in Movie Recommandation System || Campusx
from sklearn.feature_extraction.text import CountVectorizer
cv=CountVectorizer(max_features=5000,stop_words='english')

#max_feature:Keep only top 5000 most used words in entire movie dataset
#stop_words: like in, and, a, of, an will not be added these are connecting words in english

"""
“max_features=5000 means from all the tags it will take Top 5000 most frequent unique words”
✅ Correct

“then for each movie the frequency of the word will be counted”
✅ Partially correct — but let’s refine it

🔹 Correct Understanding (important)
Step 1: Build vocabulary (global)
👉 From ALL movies combined, it selects:
Top 5000 most frequent unique words
✔ This happens once globally, not per movie

Step 2: For each movie
👉 Now for each movie, it checks:
“How many times does each of those 5000 words appear in this movie?”"""

