"""7. Movie Recommended Function<br>
This will give you the similar movies

Here is the logic:
<br>
When I search for a movie (for example, Avatar), first I find its index (or movie ID) in the dataset. Then I take the vector of that movie and use cosine similarity to compare it with all other movie vectors.
<br>
This gives similarity scores between the selected movie and every other movie. After that, I sort these scores in descending order and select the top 5 highest values (excluding the movie itself).
"""

# Search the given movie name in the title column
# Pandas checks all movie titles and finds the matching row
# Get the index of that matched movie
# This index is used to access its similarity scores

def recommend(movie):
  movie_index=new_df[new_df['title']== movie].index[0]
  movies_list=sorted(list(enumerate(similarity[movie_index])),reverse=True,key=lambda x:x[1])[1:6]

  L=[]
  for i in movies_list:
    print(new_df.iloc[i[0]].title)
    L.append(new_df.iloc[i[0]].title)

